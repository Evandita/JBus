/**
 * Enumerasi {@code Type} merepresentasikan jenis voucher, yaitu REBATE (rabat) dan DISCOUNT (diskon).
 * @author Evandita
 */
public enum Type
{
    /**
     * Representasi jenis voucher REBATE (rabat).
     */
    REBATE,
    /**
     * Representasi jenis voucher DISCOUNT (diskon).
     */
    DISCOUNT;
}
