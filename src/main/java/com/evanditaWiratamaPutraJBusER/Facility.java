package com.evanditaWiratamaPutraJBusER;


/**
 * Enumeration class Facility merepresentasikan berbagai fasilitas yang mungkin tersedia di dalam sebuah bus.
 * Setiap fasilitas terdaftar sebagai konstan dalam enumerasi ini.
 * @author Evandita
 */
public enum Facility
{
    /**
     * Air conditioning (AC).
     */
    AC,
    /**
     * Layanan makan siang.
     */
    LUNCH,
    /**
     * Toilet di dalam bus.
     */
    TOILET,
    /**
     * Kotak pendingin untuk menyimpan barang pada suhu rendah.
     */
    COOL_BOX,
    /**
     * Layanan Wi-Fi.
     */
    WIFI,
    /**
     * TV LCD untuk hiburan.
     */
    LCD_TV,
    /**
     * Soket listrik untuk mengisi daya perangkat elektronik.
     */
    ELECTRIC_SOCKET,
    /**
     * Ruang untuk bagasi besar.
     */
    LARGE_BAGGAGE;
}
