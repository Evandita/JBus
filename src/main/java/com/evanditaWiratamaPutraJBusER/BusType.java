package com.evanditaWiratamaPutraJBusER;

/**
 * Kelas enumerasi BusType merepresentasikan berbagai jenis bus yang mungkin digunakan dalam sistem transportasi bus.
 * Setiap jenis bus terdaftar sebagai konstan dalam enumerasi ini.
 * @author Evandita
 */
public enum BusType
{
    /**
     * Jenis bus reguler.
     */
    REGULER,
    /**
     * Jenis bus high decker.
     */
    HIGH_DECKER,
    /**
     * Jenis bus minibis.
     */
    MINIBUS,
    /**
     * Jenis bus double decker.
     */
    DOUBLE_DECKER;
}
